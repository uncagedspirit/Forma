/// Spacing constants for the Forma design system.
///
/// Based on a 4 dp base unit.
/// See DESIGN_SYSTEM.md §4 for the complete spacing specification.
class AppSpacing {
  AppSpacing._();

  /// 4 dp — Icon gap, micro padding
  static const double xs = 4;

  /// 8 dp — Between related elements
  static const double sm = 8;

  /// 16 dp — Default padding
  static const double md = 16;

  /// 24 dp — Section padding, card padding
  static const double lg = 24;

  /// 32 dp — Large section gaps
  static const double xl = 32;

  /// 48 dp — Hero sections
  static const double xxl = 48;

  /// 22 dp — Screen horizontal padding (deliberate editorial margin)
  static const double screenHorizontal = 22;

  /// 18 dp — Content padding from top of safe area to first title
  static const double contentTop = 18;
}